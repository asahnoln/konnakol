<div class="content">
	<p>
		Your composition must be written in such a way, so that each non-spaced word would represent one
		matra.
	</p>
	<p>
		This app doesn't use any special syntax, it only parses text, looking at words and spaces,
		highlighting those words which happen to take place at the beat of a thalam.
	</p>
	<p>Examples of one-matra solkattus:</p>
	<pre>Tha
ThaKa
ThaRiKiTa
.</pre>
	<p>Examples of 2-matra solkattus:</p>
	<pre>Tha Ka
KiTa ThaKa
. Tham
Tha- am</pre>
	<p>Examples of 3-matra solkattus:</p>
	<pre>Tha Ki Ta
ThaRi KiTa ThaKa
. Tha .
Tha- an- gu</pre>
	<p>And so on.</p>
	<p>
		For example, let's see a composition written in Adhi Thalam, where we say only one sol per
		akcharam:
	</p>
	<pre>Tha . . . 
Dhi . . . 
Dhom . . . 
Nam . . .</pre>
	<p>Two evenly spaced sols (2-matra sols):</p>
	<pre>Tha . Tha . 
Dhi . Dhi . 
Dhom . Dhom . 
Nam . Nam .</pre>
	<p>All 4 matras taken in each akcharam:</p>
	<pre>Tha Tha Tha Tha 
Dhi Dhi Dhi Dhi 
Dhom Dhom Dhom Dhom 
Nam Nam Nam Nam</pre>
	<p>
		Let's see how we should write famous <code>ThaKaThaRiKiTaThaKa</code> so it takes 4 matras as well:
	</p>
	<pre>ThaKa ThaRi KiTa ThaKa</pre>
	<p>So, basically, each word is one matra, matras are separated by space.</p>
	<p>
		How to write sols that take more than 1 matra? Like <code>Thaam</code> or
		<code>Thangu</code>?
	</p>
	<p>As previous examples suggested, you could write it like this:</p>
	<pre>Tha- an- gu</pre>
	<p>Or</p>
	<pre>Tha an gu</pre>
	<p>Or</p>
	<pre>Thangu . .</pre>
	<p>Whatever you understand the best from reading.</p>
	<p>
		How to write pauses than? You could use anything from punctuation, like dots (my favorite),
		commas, dashes, etc:
	</p>
	<pre>Tha . . Dhi - - Dhom , , Nam pause pause</pre>
	<p>
		Yes, you can write <code>pause</code>. Only your creativity and understanding matters.
	</p>
</div>
